/**
 * @author Filip Vucic
 * Enum which contains {@link Token} types.
 */
public enum TokenType {

    /**
     * End of file token type.
     */
    EOF,

    /**
     * Word token type.
     */
    WORD,

    /**
     * Number token type.
     */
    NUMBER,

    /**
     * Symbol token type.
     */
    SYMBOL
}
